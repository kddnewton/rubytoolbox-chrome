// Make an SVG icon for a status
function makeStatusIcon (status) {
  var statusIcon = statusIcons[status.icon];

  var svgNode = document.createElementNS("http://www.w3.org/2000/svg", "svg");
  var pathNode = document.createElementNS("http://www.w3.org/2000/svg", "path");

  svgNode.setAttribute("class", "rtb--icon");
  svgNode.setAttribute("aria-hidden", true);
  svgNode.setAttribute("role", "img");
  svgNode.setAttribute("viewBox", statusIcon.viewBox);

  pathNode.setAttribute("fill", "currentColor");
  pathNode.setAttribute("d", statusIcon.path);
  svgNode.appendChild(pathNode);

  return svgNode;
}

// Make a node representing the status from the ruby toolbox
function makeStatusTag (status) {
  var spanNode = document.createElement("span");
  spanNode.className = "rtb--tag rtb--tag-" + status.level;

  spanNode.appendChild(makeStatusIcon(status));
  spanNode.appendChild(document.createTextNode(status.label));

  return spanNode;
}

// Make a node containing all of the rendered status tags
function makeStatusTags (response) {
  var divNode = document.createElement("div");
  divNode.className = "rtb--tags";

  response.projects[0].health.statuses.forEach(function (status) {
    divNode.appendChild(makeStatusTag(status));
  });

  return divNode;
}

// Fire off an AJAX request to get the statuses, then render them
var xhr = new XMLHttpRequest();

xhr.onreadystatechange = function () {
  if ((xhr.readyState === 4) && (xhr.status >= 200) && (xhr.status < 300)) {
    var statusTags = makeStatusTags(JSON.parse(xhr.responseText));
    document.querySelector(".page__heading").appendChild(statusTags);
  }
};

